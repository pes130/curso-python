# Las listas se ordenan con el método .sort()
lista = [7, 8, 2, 1, 2, 4, 5]

# La ordenamos
print(lista)
lista.sort()
print(lista)

# Ordenarla a la inversa
lista.sort(reverse=True)
print(lista)

lista_elementos = ['Litio', 'Sodio', 'Nitrógeno', 'Potasio', 'Cesio']
print(lista_elementos)
print(lista_elementos.sort())
print(lista_elementos)





