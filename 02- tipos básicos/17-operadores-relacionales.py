numero1 = int(input("Introduce un número A: "))
numero2 = int(input("Introduce un número B: "))

print(numero1, "==", numero2, ":", numero1==numero2)
print(numero1, "!=", numero2, ":", numero1!=numero2)
print(numero1, ">", numero2, ":", numero1>numero2)
print(numero1, ">=", numero2, ":", numero1>=numero2)
print(numero1, "<", numero2, ":", numero1<numero2)
print(numero1, "<=", numero2, ":", numero1<=numero2)